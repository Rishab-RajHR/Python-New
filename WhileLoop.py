# loops are used to repeat a block of code multiple times ( repeatation )


# Without Loop
print(1)
print(2)
print(3)


# While Loop => Used when you want to repeat something until a condition becomes false.
num = 1
while num <= 10:
  print(num)
  num += 1
print("Loop finished")


# In Reverse Order
num1 = 10
while num1 >= 1:
  print(num1)
  num1 -= 1
print("Loop finished")