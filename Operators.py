# Arithmetic Operator
num1 = 20
num2 = 10
print("Addition:", num1 + num2)
print("Subtraction:", num1 - num2)
print("Multiplication:", num1 * num2)
print("Division:", num1 / num2)
print("Floor Division:", num1 // num2) # Converts to nearest whole number
print("Modulus:", num1 % num2) 
print("Exponent:", num1 ** num2) 



# Comparison Operator // Relational Operator
num1 = 2
num2 = 4
print("Equal to:", num1 == num2)
print("Not equal to:", num1 != num2)
print("Greater than:", num1 > num2)
print("Less than:", num1 < num2)
print("Greater than or equal to:", num1 >= num2)
print("Less than or equal to:", num1 <= num2)




# Logical Operator
num1 = True
num2 = False
print("Logical AND:", num1 and num2)
print("Logical OR:", num1 or num2)
print("Logical NOT:",not num1)




